liste =[17, 38, 10, 25, 72]
liste.sort()
print("Trier : ",liste)
liste.append(12)
print("ajouter l’élément 12 : ",liste)
liste.reverse()
print("renverser : ",liste)
print("l’indice de l’élément 17 : ",liste.index(17))
liste.remove(38)
print("Enlever l’élément 38 : ",liste)
print("sous-liste du 2e au 3e élément : ",liste[1:3])
print("sous-liste du début au 2e élément : ",liste[:2])
print("sous-liste du 3e élément à la fin : ",liste[2:])
print("sous-liste complète : ",liste[:])
print("dernier élément : ",liste[-1])





    
    
